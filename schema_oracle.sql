
CREATE TABLE users (
  id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  name VARCHAR2(100),
  email VARCHAR2(100),
  phone VARCHAR2(15),
  dob DATE,
  gender VARCHAR2(20),
  address CLOB
);

CREATE TABLE employees (
  id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  userId NUMBER,
  employeeId VARCHAR2(20),
  pan VARCHAR2(10),
  bankAccount VARCHAR2(30),
  FOREIGN KEY (userId) REFERENCES users(id)
);

CREATE TABLE medical_records (
  id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  userId NUMBER,
  recordNumber VARCHAR2(20),
  diagnosis CLOB,
  FOREIGN KEY (userId) REFERENCES users(id)
);

CREATE TABLE insurance_policies (
  id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  userId NUMBER,
  policyNumber VARCHAR2(20),
  provider VARCHAR2(100),
  FOREIGN KEY (userId) REFERENCES users(id)
);
